                Quaternion([1,0,0,0]), ...
                Quaternion([0,-1.5,0,0]), ...
                Quaternion([0,-.5,0,1]), ...
                [ ...
                    KinematicTree( ... % Left upper arm
                        Quaternion([1/sqrt(2),1/sqrt(2),0,0]), ...
                        Quaternion([0,0,3,0]), ...
                        Quaternion([0,0,0,0]), ...
                        [ ...
                            KinematicTree( ... % Left forearm
                                Quaternion([1,0,0,0]), ...
                                Quaternion([0,0,3,0]), ...
                                Quaternion([0,0,0,0]), ...
                                [ ...
                                ] ...
                            ) ...
                        ] ...
                    ) ...
                ] ...
            ), ...
            KinematicTree(... % Right shoulder
                Quaternion([1,0,0,0]), ... 
                Quaternion([0,1.5,0,0]), ...
                Quaternion([0,0.5,0,1]), ...
                [ ...
                    KinematicTree( ... % Right upper arm
